# Variable is a container for a value (strin, integer, float, boolean)

#String
first_name = "Pawel"
print(f"Hello I'm {first_name}")

#Integer
age = 25
print(f"I am {age} years old")

#Float
height = 1.8
print(f"My height is {height} m")

#Boolean
is_graduate = True
if is_graduate:
    print("I am a graduate")
else:
    print("I am not a graduate")
